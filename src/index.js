levelUp() {
    if(this.health !== 0) {
    this.level++;
    this.health = 100;
    (this.attack / 100) * 0.2;
    (this.defence / 100) * 0.2;
    } else {
        throw new Error('you are dead :(')
    }
};

damage(points) {
    if(this.health !== 0) {
        this.health -= points * (1 - this.defence / 100)
    }
}